class PokemonData:
    def __init__(self,name,elementOne,elementTwo,hp,atk,defense,spatk,spdef,speed,moves):
        self.name = name
        self.elementOne = elementOne
        self.elementTwo = elementTwo        
        self.hp = hp
        self.atk = atk
        self.defense = defense
        self.spatk = spatk
        self.spdef = spdef
        self.speed = speed
        self.moves = moves

class MoveData:
    def __init__(self,name,element,basePower,split,pp,accuracy):
        self.name = name
        self.element = element
        self.type = split
        self.damage = basePower
        self.accuracy = accuracy
        self.pp = pp

